class Coord(tuple):
    def __add__(self, other):
        if isinstance(other, Coord) and len(self) == len(other):
            return Coord(i+j for i, j in zip(self, other))


def trees(inp, step: tuple):
    step = Coord(step)
    current = step
    while current[0] < len(inp):
        yield inp[current[0]][current[1] % len(inp[0])] == "#"
        current += step


